class Pipe {
  constructor(index: Position, route: string) {
    this.PipeIndex = index;
    this.PipeRoute = route;
  }

  public PipeIndex: Position;
  public PipeRoute: string;

  public toString = (): string => {
    return `I:x${this.PipeIndex.xIndex}y${this.PipeIndex.yIndex}, R:${this.PipeRoute}\n`;
  };
}

