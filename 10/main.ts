function checkRoute(current: Position): Position | false {
  let currentPipe: Pipe = new Pipe({ xIndex: 0, yIndex: 0 }, '');

  let currentFound = pipes.find(
    (x) =>
      x.PipeIndex.xIndex == current.xIndex &&
      x.PipeIndex.yIndex == current.yIndex
  );

  if (currentFound) {
    currentPipe = currentFound;
  }

  // top route
  let next: Position = {
    xIndex: current.xIndex,
    yIndex: current.yIndex - 1,
  };
  let found = pipes.find(
    (x) =>
      x.PipeIndex.xIndex == next.xIndex && x.PipeIndex.yIndex == next.yIndex
  );
  if (
    found &&
    currentPipe.PipeRoute.indexOf('N') > -1 &&
    found.PipeRoute.indexOf('S') > -1 &&
    pipesVisited.findIndex(
      (x) => x.xIndex == next.xIndex && x.yIndex == next.yIndex
    ) < 0
  ) {
    return next;
  }

  // rigth route
  next = {
    xIndex: current.xIndex + 1,
    yIndex: current.yIndex,
  };
  found = pipes.find(
    (x) =>
      x.PipeIndex.xIndex == next.xIndex && x.PipeIndex.yIndex == next.yIndex
  );
  if (
    found &&
    currentPipe.PipeRoute.indexOf('E') > -1 &&
    found.PipeRoute.indexOf('W') > -1 &&
    pipesVisited.findIndex(
      (x) => x.xIndex == next.xIndex && x.yIndex == next.yIndex
    ) < 0
  ) {
    return next;
  }

  // bottom route
  next = {
    xIndex: current.xIndex,
    yIndex: current.yIndex + 1,
  };
  found = pipes.find(
    (x) =>
      x.PipeIndex.xIndex == next.xIndex && x.PipeIndex.yIndex == next.yIndex
  );
  if (
    found &&
    currentPipe.PipeRoute.indexOf('S') > -1 &&
    found.PipeRoute.indexOf('N') > -1 &&
    pipesVisited.findIndex(
      (x) => x.xIndex == next.xIndex && x.yIndex == next.yIndex
    ) < 0
  ) {
    return next;
  }

  // left route
  next = {
    xIndex: current.xIndex - 1,
    yIndex: current.yIndex,
  };
  found = pipes.find(
    (x) =>
      x.PipeIndex.xIndex == next.xIndex && x.PipeIndex.yIndex == next.yIndex
  );
  if (
    found &&
    currentPipe.PipeRoute.indexOf('W') > -1 &&
    found.PipeRoute.indexOf('E') > -1 &&
    pipesVisited.findIndex(
      (x) => x.xIndex == next.xIndex && x.yIndex == next.yIndex
    ) < 0
  ) {
    return next;
  }

  return false;
}

